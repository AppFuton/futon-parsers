	MangaReaderParser(
		context,
		MangaParserSource.TOOMTAMMANGA,
		"toomtam-manga.com",
		pageSize = 30,
		searchPageSize = 28,
	) {
	override val filterCapabilities: MangaListFilterCapabilities
		get() = super.filterCapabilities.copy(
			isTagsExclusionSupported = false,
		)
}
